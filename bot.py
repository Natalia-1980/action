def init_db():
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute('''CREATE TABLE IF NOT EXISTS users
                 (user_id INTEGER PRIMARY KEY,
                  username TEXT,
                  first_name TEXT,
                  current_day INTEGER DEFAULT 1,
                  started_at TEXT,
                  last_activity TEXT)''')
    c.execute('''CREATE TABLE IF NOT EXISTS answers
                 (user_id INTEGER,
                  day_number INTEGER,
                  is_correct INTEGER,
                  answered_at TEXT,
                  skipped INTEGER DEFAULT 0,
                  PRIMARY KEY (user_id, day_number))''')
    c.execute('''CREATE TABLE IF NOT EXISTS last_message_id
                 (user_id INTEGER PRIMARY KEY,
                  message_id INTEGER)''')
    conn.commit()
    conn.close()

def get_user(user_id: int) -> Optional[Tuple]:
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("SELECT * FROM users WHERE user_id = ?", (user_id,))
    result = c.fetchone()
    conn.close()
    return result

def create_user(user_id: int, username: str, first_name: str):
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    now = datetime.now(TIMEZONE).isoformat()
    c.execute("INSERT INTO users (user_id, username, first_name, current_day, started_at, last_activity) VALUES (?, ?, ?, 1, ?, ?)",
              (user_id, username, first_name, now, now))
    conn.commit()
    conn.close()

def update_user_activity(user_id: int):
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    now = datetime.now(TIMEZONE).isoformat()
    c.execute("UPDATE users SET last_activity = ? WHERE user_id = ?", (now, user_id))
    conn.commit()
    conn.close()

def set_user_day(user_id: int, day: int):
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("UPDATE users SET current_day = ? WHERE user_id = ?", (day, user_id))
    conn.commit()
    conn.close()

def save_answer(user_id: int, day: int, is_correct: bool, skipped: int = 0):
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    now = datetime.now(TIMEZONE).isoformat()
    c.execute("INSERT OR REPLACE INTO answers (user_id, day_number, is_correct, answered_at, skipped) VALUES (?, ?, ?, ?, ?)",
              (user_id, day, 1 if is_correct else 0, now, skipped))
    conn.commit()
    conn.close()

def get_answer_status(user_id: int, day: int) -> Optional[Tuple]:
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("SELECT is_correct, skipped FROM answers WHERE user_id = ? AND day_number = ?", (user_id, day))
    result = c.fetchone()
    conn.close()
    return result

def get_stats(user_id: int) -> Tuple[int, int, int]:
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("SELECT COUNT(*), SUM(is_correct) FROM answers WHERE user_id = ? AND skipped = 0", (user_id,))
    total, correct = c.fetchone()
    c.execute("SELECT COUNT(*) FROM answers WHERE user_id = ? AND skipped = 1", (user_id,))
    skipped = c.fetchone()[0]
    conn.close()
    return (total or 0, correct or 0, skipped or 0)

def get_week_stats(user_id: int, week: int) -> Tuple[int, int]:
    days_start = (week - 1) * 7 + 1
    days_end = min(week * 7, 30)
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("SELECT COUNT(*), SUM(is_correct) FROM answers WHERE user_id = ? AND day_number BETWEEN ? AND ? AND skipped = 0",
              (user_id, days_start, days_end))
    total, correct = c.fetchone()
    conn.close()
    return (total or 0, correct or 0)

def save_last_message_id(user_id: int, message_id: int):
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("INSERT OR REPLACE INTO last_message_id (user_id, message_id) VALUES (?, ?)", (user_id, message_id))
    conn.commit()
    conn.close()

def get_last_message_id(user_id: int) -> Optional[int]:
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("SELECT message_id FROM last_message_id WHERE user_id = ?", (user_id,))
    result = c.fetchone()
    conn.close()
    return result[0] if result else None

def reset_last_message_id(user_id: int):
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("DELETE FROM last_message_id WHERE user_id = ?", (user_id,))
    conn.commit()
    conn.close()

