async def handle_text(message: types.Message, bot: Bot):
    user_id = message.from_user.id
    text = message.text.strip()
    
    # Проверяем, не является ли текст номером ответа
    if text in ["1", "2", "3"]:
        user = get_user(user_id)
        if not user:
            await cmd_start(message, bot)
            return
        
        current_day = user[3]
        update_user_activity(user_id)
        
        # Проверяем, не отвечал ли уже пользователь
        status = get_answer_status(user_id, current_day)
        if status is not None:
            await send_message(user_id, bot, f"⚠️ Вы уже ответили на <b>День {current_day}</b>. Используйте /next, чтобы перейти к следующему дню.", get_main_keyboard())
            return
        
        choice = int(text)
        data = SITUATIONS[current_day]
        is_correct = (choice == data['correct'])
        
        # Сохраняем ответ
        save_answer(user_id, current_day, is_correct)
        
        # Отправляем ответ
        await send_answer(user_id, bot, current_day, is_correct, choice)
        
        # Если это был день 7, 14, 21 — отправляем статистику недели
        if current_day in [7, 14, 21, 30]:
            await send_stats(user_id, bot)
        
        # Автоматически переходим к следующему дню после ответа (опционально, можно закомментировать)
        # В данном случае не переходим автоматически, чтобы пользователь мог посмотреть ответ и потом сам перейти
    else:
        # Неизвестная команда
        await send_message(user_id, bot, "❓ Неизвестная команда. Отправьте /help для списка команд.", get_main_keyboard())

