async def morning_mailing(bot: Bot):
    """Отправляет утреннюю рассылку всем пользователям"""
    conn = sqlite3.connect('users.db')
    c = conn.cursor()
    c.execute("SELECT user_id, current_day FROM users")
    users = c.fetchall()
    conn.close()
    
    for user_id, current_day in users:
        # Пропускаем, если день уже завершен
        if current_day > 30:
            continue
        
        # Проверяем, не отвечал ли уже пользователь на этот день
        status = get_answer_status(user_id, current_day)
        if status is not None:
            continue
        
        try:
            await send_situation(user_id, bot, current_day)
            logger.info(f"Утренняя рассылка отправлена пользователю {user_id}, день {current_day}")
            await asyncio.sleep(0.5)  # Небольшая задержка между отправками
        except Exception as e:
            logger.error(f"Ошибка отправки утренней рассылки {user_id}: {e}")

async def scheduler(bot: Bot):
    """Планировщик задач"""
    while True:
        now = datetime.now(TIMEZONE)
        target_time = now.replace(hour=MORNING_HOUR, minute=MORNING_MINUTE, second=0, microsecond=0)
        
        # Если текущее время больше целевого, ждем до следующего дня
        if now >= target_time:
            target_time = target_time + timedelta(days=1)
        
        wait_seconds = (target_time - now).total_seconds()
        logger.info(f"Следующая рассылка через {wait_seconds/3600:.1f} часов")
        
        await asyncio.sleep(wait_seconds)
        
        # Выполняем рассылку
        await morning_mailing(bot)
        
        # Ждем еще немного, чтобы не отправить дважды
        await asyncio.sleep(60)

